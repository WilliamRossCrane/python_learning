# A comment starts with a # symbol.
# Python ignores comments.
# Comments are notes that help humans understand the code.

# print() shows a message in the terminal.
print("Hello, Python!")

# The text inside quotation marks is called a string.
# A string is just text.
print("My name is Will.")

# Python runs code from top to bottom.
# This line runs after the lines above it.
print("I am learning Python fundamentals.")

# We can use print() more than once.
# Each print() appears on a new line.
print("Python is used for apps, games, websites, data, AI, and automation.")

# This final message reminds us that coding is learned step by step.
print("Small steps make big programs.")